package in.sel.model;

public class M_CheckedList {

	int postion;
	int value;

	public M_CheckedList(int postion, int value) {
		super();
		this.postion = postion;
		this.value = value;
	}

	public int getPostion() {
		return postion;
	}

	public void setPostion(int postion) {
		this.postion = postion;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
}
